let fs = require('fs');
let input = fs.readFileSync(0, 'utf8').trim().split(/\s+/);
let N = Number(input[0]);
let M = Number(input[1]);
console.log(`${N} + ${M} = ${N + M}`);
console.log(`${N} - ${M} = ${N - M}`);
console.log(`${N} * ${M} = ${N * M}`);
console.log(`${N} / ${M} = ${Math.floor(N / M)}`);
console.log(`${N} % ${M} = ${N % M}`);